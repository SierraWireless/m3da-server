package m3da.server.api.json;

/**
 * TODO Comment this class
 */
public class JSystemWriteData {

	private String key;
	private Object value;

	/**
	 * Dummy constructor needed by jackson
	 */
	public JSystemWriteData() {
	}

	/**
	 * @param key
	 * @param value
	 */
	public JSystemWriteData(String key, Object value) {
		super();
		this.key = key;
		this.value = value;
	}

	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}

	/**
	 * @param key
	 *            the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}

	/**
	 * @return the value
	 */
	public Object getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(Object value) {
		this.value = value;
	}

}
